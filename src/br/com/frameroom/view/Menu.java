    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
               // botao registrar


        try {
            Espacos obj = new Espacos();
            EspacosDAO dao = new EspacosDAO();

            //Verificando se todos os campos estao preenchidos
            if(txtCapacidade.getText().isEmpty() ||  txtNumero.getText().isEmpty() || txtDescricao.getText().isEmpty()){

                JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            }


            // Cadastra no banco
            else{


                // Salvando dados
                obj.setCod_espaco(selectTipo.getSelectedItem().toString().substring(0, 3).toUpperCase() + txtNumero.getText());
                obj.setTipo_espaco(selectTipo.getSelectedItem().toString());
                obj.setNumero(parseInt(txtNumero.getText()));
                obj.setNome(selectTipo.getSelectedItem().toString() + " " + txtNumero.getText());
                obj.setCapacidade(parseInt(txtCapacidade.getText()));
                obj.setAndar(selectAndar.getSelectedItem() == "1" ? 1 : 2);
                obj.setDescricao(txtDescricao.getText());

                dao.cadastrarEspaco(obj);

                new Utilitarios().LimpaTela(jPanel16);
                
            }

   

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnEnviarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarReservaActionPerformed
       // botao enviar reserva

        try {
            Reservas obj = new Reservas();

            //Verificando se todos os campos estao preenchidos
            if(txtMensagem.getText().isEmpty() ||  selectData.getDate() == null){

                JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            }


            // Cadastra no banco
            else{

                java.sql.Date sqldate = new java.sql.Date(selectData.getDate().getTime());
                //DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
                //String date = formatter.format(sqldate);
                
                // Salvando dados
                obj.setData(sqldate);
                obj.setHora_inicio(selectHoraInicio.getSelectedItem().toString());
                obj.setHora_fim(selectHoraFim.getSelectedItem().toString());
                obj.setUsuario_cpf(usuarioLogadoCpf);
                obj.setMensagem(txtMensagem.getText());
                

                
                //Criando objeto de espaco
                Espacos e = new Espacos();
                e = (Espacos) selectEspaco.getSelectedItem();                
                
                obj.setEspaco(e);
                

                
                
                ReservasDAO dao = new ReservasDAO();
                dao.cadastrarReservas(obj);
                

                new Utilitarios().LimpaTela(jPanel12);
                selectHoraFim.setSelectedIndex(0);
                selectHoraFim.setSelectedIndex(0);
                selectHoraFim.setSelectedIndex(0);
                selectData.setDate(null);
                txtMensagem.setText("");
                
            }

   

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e);
        }
    }//GEN-LAST:event_btnEnviarReservaActionPerformed

