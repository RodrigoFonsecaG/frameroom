    public void listarEspacos(){
        
        EspacosDAO dao = new EspacosDAO();
        
        List<Espacos> lista = dao.listarEspacos();
        DefaultTableModel dados = (DefaultTableModel) tabelaEspacos.getModel();
        
        dados.setNumRows(0);
        
        for(Espacos c: lista){
            dados.addRow(new Object[]{
                c.getCod_espaco(),
                c.getTipo_espaco(),
                c.getNumero(),
                c.getNome(),
                c.getCapacidade(),
                c.getAndar(),
                c.getDescricao()
            });
        }
        
    }
    
     public void listarReservas(){
        
        ReservasDAO dao = new ReservasDAO();
        
        List<Reservas> lista = dao.listarReservas();
        DefaultTableModel dados = (DefaultTableModel) tabelaReservas.getModel();
        
        dados.setNumRows(0);
        
        DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");

        
        for(Reservas c: lista){
            dados.addRow(new Object[]{
                c.getUsuario().getCargo(),
                c.getUsuario().getNome(),
                c.getEspaco().getNome(),
                formatter.format(c.getData()),
                c.getHora_inicio() + " a " + c.getHora_fim(),
                c.getMensagem(),
                c.getUsuario().getEmail(),
                c.getUsuario().getTelefone(),
                c.getCod_reserva()
            });
        }
        
    }

